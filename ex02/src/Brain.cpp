Brain::Brain(void) { }

Brain::Brain(const Brain &brain)
{
	*this = brain;
}

/* -------------------------------------------------------------------------- */
/*                                 Destructors                                */
/* -------------------------------------------------------------------------- */

Brain::~Brain(void) { }

/* -------------------------------------------------------------------------- */
/*                        Assignement operator overload                       */
/* -------------------------------------------------------------------------- */

Brain	&Brain::operator=(const Brain &brain)
{
	if (this == &brain)
		return (*this);
	for (size_t i = 0; i < IDEA_COUNT; i++)
		ideas[i] = brain.ideas[i];
	return (*this);
}
