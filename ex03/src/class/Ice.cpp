/* -------------------------------------------------------------------------- */
/*                        Assignement operator overload                       */
/* -------------------------------------------------------------------------- */

Ice	&Ice::operator=(const Ice &o)
{
	*static_cast<AMateria *>(this) = o;
	return (*this);
}

